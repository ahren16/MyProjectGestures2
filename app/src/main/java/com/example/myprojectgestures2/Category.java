package com.example.myprojectgestures2;

public class Category {
    public long id;
    public String name;

    public String shortName;
    public String color;

    public Category(long id, String name, String shortName, String color) {
        this.id = id;
        this.name = name;
        this.shortName = shortName;
        this.color = color;
    }
}
