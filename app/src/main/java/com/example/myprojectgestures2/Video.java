package com.example.myprojectgestures2;

public class Video {
    public String name;
    public String code;
    public long id;
    public String comments;
    public String  URL;
    public int version;
    public String filename;

    public Video(String name, String code, long id, String comments, String URL, int version, String filename) {
        this.name = name;
        this.code = code;
        this.id = id;
        this.comments = comments;
        this.URL = URL;
        this.version = version;
        this.filename = filename;
    }
}
